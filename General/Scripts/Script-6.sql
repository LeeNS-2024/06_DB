CREATE OR REPLACE FUNCTION NEXT_BOARD_IMG_NO

-- 반환형
RETURN NUMBER

-- 사용할 변수
IS BOARD_IMG_NO NUMBER;

BEGIN 
	SELECT SEQ_BOARD_IMG_NO.NEXTVAL 
	INTO BOARD_IMG_NO
	FROM DUAL;

	RETURN BOARD_IMG_NO;
END;
;

